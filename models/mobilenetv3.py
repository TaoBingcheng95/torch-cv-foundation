"""
V3 并没有推翻 V2，而是在 V2 的基础上引入了三个关键升级：
引入 SE 注意力模块 (Squeeze-and-Excitation)：
在 V2 的倒残差模块中间，塞入了一个轻量级的 SE 模块。它能让网络学会“关注重要的通道，忽略不重要的通道”（通道注意力机制）。
更换激活函数 (ReLU -> h-swish)：
V2 大量使用 ReLU6。V3 引入了 h-swish 激活函数（Swish 函数的硬件友好近似版）。它在深层网络中表现更好，且计算量比标准的 Swish 小。
NAS (神经架构搜索) 与 NetAdapt：
这是最大的区别！ V2 的结构是作者基于数学直觉手工推导的；而 V3 的整体层级结构（每一层用几个 block、通道数多少、SE 加在哪）是用强化学习算法 (NAS) 在 GPU 上自动搜索出来的，然后再用 NetAdapt 算法微调。因此，V3 分为 Large 和 Small 两个版本，结构看起来不如 V2 那么“规整”。
"""

